CREATE TABLE configs (
    id INTEGER NOT NULL PRIMARY KEY AUTOINCREMENT,
    name VARCHAR NOT NULL,
    user VARCHAR NULL,
    shared INTEGER NULL REFERENCES configs(id) ON UPDATE CASCADE ON DELETE RESTRICT,
    share_hotkey BOOLEAN NOT NULL DEFAULT FALSE,
    share_windows_settings BOOLEAN NOT NULL DEFAULT FALSE,
    share_vault BOOLEAN NOT NULL DEFAULT FALSE,
    content TEXT NOT NULL DEFAULT '',
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    modified_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);